package org.baeldung.persistence.service;

public class TiLeDto {
	 private String chan;
	 private String cuale;
	 private String bonden;
	 private String tragbaden;
	 private String denbatrang;
	 private String bontrang;
	public String getChan() {
		return chan;
	}
	public void setChan(String chan) {
		this.chan = chan;
	}
	public String getCuale() {
		return cuale;
	}
	public void setCuale(String cuale) {
		this.cuale = cuale;
	}
	public String getBonden() {
		return bonden;
	}
	public void setBonden(String bonden) {
		this.bonden = bonden;
	}
	public String getTragbaden() {
		return tragbaden;
	}
	public void setTragbaden(String tragbaden) {
		this.tragbaden = tragbaden;
	}
	public String getDenbatrang() {
		return denbatrang;
	}
	public void setDenbatrang(String denbatrang) {
		this.denbatrang = denbatrang;
	}
	public String getBontrang() {
		return bontrang;
	}
	public void setBontrang(String bontrang) {
		this.bontrang = bontrang;
	}
	 
	 
	
	 
}
